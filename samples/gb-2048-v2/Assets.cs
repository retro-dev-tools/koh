    public static byte FontBase;

    public static void Load()
    {
        Tiles = TileAsset.Define(TileArt);
        Tiles.Load(0);
        FontBase = Tiles.TileCount;
        Font.LoadDefault(FontBase);
        Palettes.SetBg(0, Rgb.White, Rgb.Make(20, 25, 20), Rgb.Make(8, 14, 8), Rgb.Black, 0xE4);
