using Koh.GameBoy;

namespace Koh.GameBoy.Graphics;

/// <summary>
/// The built-in ROM ASCII font (graphics-library design doc §3 "Font.cs / Text.cs") — closes a
/// confirmed hole (neither existing sample can render a score at all). 96 glyphs, one per printable
/// ASCII code point 0x20 ('&#32;', space) through 0x7F inclusive (0x7F is DEL, not normally printable;
/// it renders as a solid block placeholder so every byte 0x20-0x7F a <see cref="Text"/> caller might
/// pass has SOME glyph rather than reading past the table).
///
/// <b>Storage:</b> <see cref="Glyphs"/> is 1bpp (8 bytes/tile — one bit per pixel, MSB = leftmost column,
/// same convention <see cref="TileSet.Load1bpp"/> documents), 768 bytes total for the whole table versus
/// 1536 for a pre-expanded 2bpp font — exactly the ROM-size win <see cref="TileSet"/>'s class remarks
/// call out. <c>static readonly</c> with a literal initializer, so it lands in
/// <c>AddressSpace.Rom</c> automatically (design doc §2, "ROM data by convention") — dropping
/// <c>readonly</c> would silently move 768 bytes into scarce WRAM.
///
/// <b>Glyph design:</b> a plain 5x7 dot-matrix letterform in an 8x8 cell (columns 1-5, rows 0-6; column
/// 0 and columns 6-7 are blank margin so adjacent characters read as separate glyphs when tiled
/// edge-to-edge with no gap; row 7 is a blank baseline row). Digits are drawn as classic seven-segment
/// shapes; <b>lowercase a-z intentionally reuse their uppercase glyph's bitmap</b> (a deliberate
/// minimalism — this is a "recognizable, built-in" font per the design doc, not a typographically
/// distinct upper/lower case face; a game wanting true lowercase forms supplies its own table via
/// <see cref="TileSet.Load1bpp"/> directly).
///
/// <b>Ink/paper:</b> <see cref="LoadDefault"/> expands with ink = color 3 (darkest/black) and paper =
/// color 0 (lightest/white) — legible dark-on-light text against <see cref="Video.Init"/>'s default
/// grayscale/0xE4 palette without any extra palette call. A game wanting a different look loads its own
/// table via <see cref="TileSet.Load1bpp"/> with different ink/paper instead of calling this method.
/// </summary>
public static class Font
{
    /// <summary>0x20 (space) is the first codepoint the table covers; <see cref="Text"/> maps a
    /// character to a glyph index via <c>(byte)ch - FirstCodePoint</c>.</summary>
    internal const byte FirstCodePoint = 0x20;

    /// <summary>96 glyphs, 0x20..0x7F inclusive.</summary>
    internal const byte GlyphCount = 96;

    /// <summary>Ink used by <see cref="LoadDefault"/> — color 3 (darkest on a standard ramp).</summary>
    private const byte DefaultInk = 3;

    /// <summary>Paper used by <see cref="LoadDefault"/> — color 0 (lightest on a standard ramp).</summary>
    private const byte DefaultPaper = 0;

    /// <summary>The VRAM tile index <see cref="LoadDefault"/> was last called with — what
    /// <see cref="Text"/> adds a glyph's table offset to. Zero (a legal tile index) until
    /// <see cref="LoadDefault"/> runs; a <see cref="Text"/> call before that draws from tile 0 onward,
    /// same "caller's responsibility to author before drawing" stance as every other module here.</summary>
    internal static byte FirstTile;

    /// <summary>Loads the built-in 96-glyph font into VRAM starting at tile <paramref name="firstTile"/>
    /// (via <see cref="TileSet.Load1bpp"/> — LCD-off straight expansion, LCD-on immediate-checked
    /// per-row, see that method's remarks) and remembers <paramref name="firstTile"/> so
    /// <see cref="Text"/> can map a character to a VRAM tile index.</summary>
    public static void LoadDefault(byte firstTile)
    {
        FirstTile = firstTile;
        TileSet.Load1bpp(firstTile, Glyphs, DefaultInk, DefaultPaper, GlyphCount);
    }

